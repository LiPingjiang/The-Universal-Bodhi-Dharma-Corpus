count_b = 0  # 文献B处理计数
errors = []

for old_num, xb_num, name_a, name_b in mapping:
    xid = f"X{old_num}"
    xbid = f"XB{xb_num:02d}"
    
    # --- 文献A：繁体前缀文件 → XB新目录 ---
    xb_dir = os.path.join(BASE, f"{xbid}_{name_a}")
    os.makedirs(xb_dir, exist_ok=True)
    
    for suffix, std_name in [
        ("_原文.md", "原文.md"),
        ("_解讀_學術.md", "解讀_學術.md"),
        ("_解讀_白話.md", "解讀_白話.md"),
    ]:
        src = os.path.join(BASE, f"{xid}_{name_a}{suffix}")
        dst = os.path.join(xb_dir, std_name)
        if os.path.exists(src):
            if os.path.exists(dst):
                # 已存在标准文件，加后缀保留
                dst = os.path.join(xb_dir, f"{name_a}{suffix}")
            shutil.move(src, dst)
            count_a += 1
        else:
            errors.append(f"文件不存在: {src}")
    
    # --- 文献B：简体裸名md → 已有目录作为索引卡 ---
    # 查找已有目录
    b_dirs = [d for d in os.listdir(BASE) if d.startswith(f"{xid}_") and os.path.isdir(os.path.join(BASE, d)) and name_b in d]
    if b_dirs:
        b_dir = os.path.join(BASE, b_dirs[0])
        b_src = os.path.join(BASE, f"{xid}_{name_b}.md")
        b_dst = os.path.join(b_dir, "索引卡.md")
        if os.path.exists(b_src):
            if os.path.exists(b_dst):
                b_dst = os.path.join(b_dir, f"{xid}_{name_b}_索引卡.md")
            shutil.move(b_src, b_dst)
            count_b += 1
    else:
        # 文献B目录不存在，检查散落md是否存在
        b_src = os.path.join(BASE, f"{xid}_{name_b}.md")
        if os.path.exists(b_src):
            errors.append(f"文献B目录不存在但md存在: {b_src}")

