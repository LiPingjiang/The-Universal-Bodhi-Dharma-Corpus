    doc_dir = os.path.join(base, doc_id)
    if not os.path.isdir(doc_dir):
        print(f"WARNING: {doc_dir} not found, skipping")
        return False
    
    academic_path = os.path.join(doc_dir, "解讀_學術.md")
    vernacular_path = os.path.join(doc_dir, "解讀_白話.md")
    
    with open(academic_path, 'w', encoding='utf-8') as f:
        f.write(academic_content)
    print(f"{doc_id} 學術解讀已寫入")
    
    with open(vernacular_path, 'w', encoding='utf-8') as f:
        f.write(vernacular_content)
    print(f"{doc_id} 白話解讀已寫入")
    
    print(f"{doc_id} done")
    return True

